



-- Q1: ACTIVE VS INACTIVE DISTRIBUTION BY CITY & OVERALL
-- Relations: FACT → DIM_STATUS, FACT → DIM_CITY

-- Q1a: Segregated by city
SELECT
    C.CITY_NAME,
    S.STATUS_GROUP,
    COUNT(*) AS LICENSE_COUNT
FROM FACT_BUSINESS_LICENSE F
JOIN DIM_STATUS S ON F.STATUS_SK = S.STATUS_SK
JOIN DIM_CITY   C ON F.CITY_SK   = C.CITY_SK
GROUP BY C.CITY_NAME, S.STATUS_GROUP
ORDER BY C.CITY_NAME, S.STATUS_GROUP;

-- Q1b: Aggregated across all cities
SELECT
    S.STATUS_GROUP,
    COUNT(*) AS LICENSE_COUNT
FROM FACT_BUSINESS_LICENSE F
JOIN DIM_STATUS S ON F.STATUS_SK = S.STATUS_SK
GROUP BY S.STATUS_GROUP
ORDER BY S.STATUS_GROUP;


-- Q2: PREDOMINANT INDUSTRIES FOR BUSINESS LICENSES
-- Relations: FACT → DIM_INDUSTRY
SELECT
    I.INDUSTRY_GROUP,
    I.INDUSTRY_LABEL,
    I.LICENSE_TYPE,
    COUNT(*) AS LICENSE_COUNT
FROM FACT_BUSINESS_LICENSE F
JOIN DIM_INDUSTRY I ON F.INDUSTRY_SK = I.INDUSTRY_SK
GROUP BY I.INDUSTRY_GROUP, I.INDUSTRY_LABEL, I.LICENSE_TYPE
ORDER BY LICENSE_COUNT DESC;


-- Q3: TOP 5 GEOGRAPHIC AREAS (BY ZIP CODE & STREET NAME) PER CITY
-- Relations: FACT → DIM_LOCATION, FACT → DIM_CITY


-- Q3a: Top 5 zip codes per city
SELECT *
FROM (
    SELECT
        C.CITY_NAME,
        L.ZIP,
        COUNT(*) AS LICENSE_COUNT,
        ROW_NUMBER() OVER (PARTITION BY C.CITY_NAME ORDER BY COUNT(*) DESC) AS RNK
    FROM FACT_BUSINESS_LICENSE F
    JOIN DIM_LOCATION L ON F.LOCATION_SK = L.LOCATION_SK
    JOIN DIM_CITY     C ON F.CITY_SK     = C.CITY_SK
    GROUP BY C.CITY_NAME, L.ZIP
) RANKED
WHERE RNK <= 5
ORDER BY CITY_NAME, RNK;

-- Q3b: Top 5 street names per city
SELECT *
FROM (
    SELECT
        C.CITY_NAME,
        L.STREET_NAME,
        COUNT(*) AS LICENSE_COUNT,
        ROW_NUMBER() OVER (PARTITION BY C.CITY_NAME ORDER BY COUNT(*) DESC) AS RNK
    FROM FACT_BUSINESS_LICENSE F
    JOIN DIM_LOCATION L ON F.LOCATION_SK = L.LOCATION_SK
    JOIN DIM_CITY     C ON F.CITY_SK     = C.CITY_SK
    GROUP BY C.CITY_NAME, L.STREET_NAME
) RANKED
WHERE RNK <= 5
ORDER BY CITY_NAME, RNK;



-- Q4: TREND IN ISSUANCE OF BUSINESS LICENSES OVER TIME
-- Relations: FACT → DIM_DATE (via ISSUE_DATE_SK)


-- Q4a: Yearly trend
SELECT
    D.YEAR,
    COUNT(*) AS LICENSES_ISSUED
FROM FACT_BUSINESS_LICENSE F
JOIN DIM_DATE D ON F.ISSUE_DATE_SK = D.DATE_SK
GROUP BY D.YEAR
ORDER BY D.YEAR;

-- Q4b: Monthly trend
SELECT
    D.YEAR,
    D.MONTH,
    D.MONTH_NAME,
    COUNT(*) AS LICENSES_ISSUED
FROM FACT_BUSINESS_LICENSE F
JOIN DIM_DATE D ON F.ISSUE_DATE_SK = D.DATE_SK
GROUP BY D.YEAR, D.MONTH, D.MONTH_NAME
ORDER BY D.YEAR, D.MONTH;



-- Q5: TRENDS IN ABANDONMENT OF BUSINESS LICENSES
-- Relations: FACT → DIM_STATUS, FACT → DIM_DATE (via STATUS_CHANGE_DATE_SK)


SELECT
    D.YEAR,
    D.QUARTER,
    S.RAW_STATUS,
    COUNT(*) AS ABANDONED_COUNT
FROM FACT_BUSINESS_LICENSE F
JOIN DIM_STATUS S ON F.STATUS_SK              = S.STATUS_SK
JOIN DIM_DATE   D ON F.STATUS_CHANGE_DATE_SK  = D.DATE_SK
WHERE S.RAW_STATUS IN ('Abandoned', 'Expired', 'Cancelled', 'Revoked')
   OR S.STATUS_GROUP = 'Inactive'
GROUP BY D.YEAR, D.QUARTER, S.RAW_STATUS
ORDER BY D.YEAR, D.QUARTER, ABANDONED_COUNT DESC;


