-- Q7: URGENCY CATEGORY COUNTS ACROSS ALL CITIES
-- Relations: FACT → DIM_CITY (URGENCY is on FACT table)


-- Q7a: Overall urgency distribution
SELECT
    F.URGENCY,
    COUNT(*) AS LICENSE_COUNT
FROM FACT_BUSINESS_LICENSE F
GROUP BY F.URGENCY
ORDER BY
    CASE F.URGENCY
        WHEN 'Immediate' THEN 1
        WHEN 'High'      THEN 2
        WHEN 'Medium'    THEN 3
        WHEN 'Low'       THEN 4
        WHEN 'Unknown'   THEN 5
    END;

-- Q7b: Urgency distribution by city
SELECT
    C.CITY_NAME,
    F.URGENCY,
    COUNT(*) AS LICENSE_COUNT
FROM FACT_BUSINESS_LICENSE F
JOIN DIM_CITY C ON F.CITY_SK = C.CITY_SK
GROUP BY C.CITY_NAME, F.URGENCY
ORDER BY C.CITY_NAME,
    CASE F.URGENCY
        WHEN 'Immediate' THEN 1
        WHEN 'High'      THEN 2
        WHEN 'Medium'    THEN 3
        WHEN 'Low'       THEN 4
        WHEN 'Unknown'   THEN 5
    END;


-- Q8: INACTIVE BUSINESSES WITH CONTACT DETAILS FOR RENEWAL REMINDERS
-- Relations: FACT → DIM_STATUS, FACT → DIM_BUSINESS, FACT → DIM_CITY

SELECT
    C.CITY_NAME,
    B.LEGAL_NAME,
    B.DBA_NAME,
    B.CONTACT_PHONE,
    B.CONTACT_EMAIL,
    S.RAW_STATUS,
    S.STATUS_GROUP
FROM FACT_BUSINESS_LICENSE F
JOIN DIM_STATUS   S ON F.STATUS_SK   = S.STATUS_SK
JOIN DIM_BUSINESS B ON F.BUSINESS_SK = B.BUSINESS_SK
JOIN DIM_CITY     C ON F.CITY_SK     = C.CITY_SK
WHERE S.STATUS_GROUP = 'Inactive'
ORDER BY C.CITY_NAME, B.LEGAL_NAME;



-- Q9: TOP 10 ORGANIZATIONS WITH HIGHEST FRANCHISE OWNERSHIPS BY CITY
-- Relations: FACT → DIM_BUSINESS, FACT → DIM_CITY
--            (FRANCHISE_PROXY is on FACT table)

SELECT *
FROM (
    SELECT
        C.CITY_NAME,
        B.LEGAL_NAME,
        MAX(F.FRANCHISE_PROXY) AS FRANCHISE_LOCATION_COUNT,
        ROW_NUMBER() OVER (
            PARTITION BY C.CITY_NAME
            ORDER BY MAX(F.FRANCHISE_PROXY) DESC
        ) AS RNK
    FROM FACT_BUSINESS_LICENSE F
    JOIN DIM_BUSINESS B ON F.BUSINESS_SK = B.BUSINESS_SK
    JOIN DIM_CITY     C ON F.CITY_SK     = C.CITY_SK
    GROUP BY C.CITY_NAME, B.LEGAL_NAME
) RANKED
WHERE RNK <= 10
ORDER BY CITY_NAME, RNK;



-- Q10: DISTRIBUTION OF BUSINESSES BY YEARS SINCE LICENSE ISSUANCE
-- Relations: FACT → DIM_DATE (via ISSUE_DATE_SK)


SELECT
    (YEAR(CURRENT_DATE) - D.YEAR) AS YEARS_SINCE_ISSUANCE,
    COUNT(*) AS LICENSE_COUNT
FROM FACT_BUSINESS_LICENSE F
JOIN DIM_DATE D ON F.ISSUE_DATE_SK = D.DATE_SK
GROUP BY (YEAR(CURRENT_DATE) - D.YEAR)
ORDER BY YEARS_SINCE_ISSUANCE;

